package com.example.myapplication;

public class Player {
    public String fio;
    public String gender;
    public String course;
    public int difficulty;
    public String birthDate;
    public String zodiac;

    public Player(String fio, String gender, String course, int difficulty, String birthDate, String zodiac) {
        this.fio = fio;
        this.gender = gender;
        this.course = course;
        this.difficulty = difficulty;
        this.birthDate = birthDate;
        this.zodiac = zodiac;
    }
}
